	 * @return mixed
	 */
	public function getSpecialDayId()
	{
		return $this->special_day_id;
	}

	/**
	 * @param mixed $special_day_id
	 */
	public function setSpecialDayId($special_day_id)
	{
		$this->special_day_id = $special_day_id;
	}

